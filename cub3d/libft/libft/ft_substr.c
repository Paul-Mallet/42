#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*dsa;

	if (s == NULL)
		return (NULL);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	dsa = malloc(len + 1);
	if (dsa == NULL)
		return (NULL);
	ft_strlcpy(dsa, s + start, len + 1);
	dsa[len] = '\0';
	return (dsa);
}
